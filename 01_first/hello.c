#include <stdio.h>

int main(void)
{
    // 声明 declaration
    int number;

    // 定义 definition
    unsigned int number_2;

    // 赋值 assignment
    number = 123;

    // 行注释，一般写在语句的上方或者定义变量时写在变量定义的后方
    printf("Hello World!\n");

    unsigned int positiveNumber = -123;

    printf("positive number = %u \n", positiveNumber);

    return 0;
}